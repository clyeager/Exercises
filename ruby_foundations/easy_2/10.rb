#create an Enumerable#each_cons method that takes a number to specify amount
# of elements that should be processed

def each_cons(arr, n)
  arr.each_with_index do |el, idx|
    break if idx + n > arr.size
    if n < 3
      yield(el, arr[idx + 1])
    else
      yield([el, arr.slice((idx + 1), (n - 1))].flatten)
    end
  end
end

hash = {}
each_cons([1, 3, 6, 10], 1) do |value|
  hash[value] = true
end
p hash == { 1 => true, 3 => true, 6 => true, 10 => true }

hash = {}
each_cons([1, 3, 6, 10], 2) do |value1, value2|
  hash[value1] = value2
end
p hash == { 1 => 3, 3 => 6, 6 => 10 }

hash = {}
each_cons([1, 3, 6, 10], 3) do |value1, *values|
  hash[value1] = values
end
p hash == { 1 => [3, 6], 3 => [6, 10] }

