class FixedArray
  attr_reader :array

  def initialize(n)
    @array = Array.new(n)
  end

  def [](idx)
    if idx > (array.size - 1)
      raise IndexError
    else
      array[idx]
    end
  end

  def []=(idx, el)
    if idx > (array.size - 1)
      raise IndexError
    else
      array[idx] = el
    end
  end

  def to_a
    array.dup
  end

  def to_s
    array.dup.to_s
  end
end

